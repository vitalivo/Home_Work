// Функция для возведения x в степень n
const power = (x, n) => {
    // Проверяем, что n является натуральным числом (целое положительное число)
    if (n <= 0 || !Number.isInteger(n)) {
        console.error("Степень должна быть натуральным числом (целым положительным).");
        return null;
    }

    let result = 1; // Инициализируем результат как 1

    // Умножаем x на себя n раз
    for (let i = 0; i < n; i++) {
        result *= x;
    }

    return result; // Возвращаем результат
};

// Примеры тестирования функции
console.log(power(2, 3)); // 2^3 = 8
console.log(power(5, 4)); // 5^4 = 625
console.log(power(3, 1)); // 3^1 = 3
console.log(power(10, 0)); // Ошибка: Степень должна быть натуральным числом
console.log(power(7, 2)); // 7^2 = 49
function isPrimeNumber(num) {
    // Проверяем, что число находится в допустимом диапазоне
    if (num > 1000) {
        console.log("Данные неверны. Введите число не больше 1000.");
        return;
    }

    // Особые случаи: 0 и 1 не являются простыми числами
    if (num === 0 || num === 1) {
        console.log(`${num} не является простым числом.`);
        return;
    }

    // Проверяем, является ли число простым
    let isPrime = true; // Предполагаем, что число простое

    for (let i = 2; i <= Math.sqrt(num); i++) {
        if (num % i === 0) {
            isPrime = false; // Если число делится на i, оно не простое
            break;
        }
    }

    // Выводим результат
    if (isPrime) {
        console.log(`${num} является простым числом.`);
    } else {
        console.log(`${num} не является простым числом.`);
    }
}

// Примеры вызова функции
isPrimeNumber(0);    // 0 не является простым числом.
isPrimeNumber(1);    // 1 не является простым числом.
isPrimeNumber(2);    // 2 является простым числом.
isPrimeNumber(17);   // 17 является простым числом.
isPrimeNumber(18);   // 18 не является простым числом.
isPrimeNumber(1001); // Данные неверны. Введите число не больше 1000.